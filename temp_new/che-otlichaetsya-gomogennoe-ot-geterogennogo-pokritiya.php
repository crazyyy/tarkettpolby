 <h1>Чем отличается Гомогенное от Гетерогенного покрытия</h1>
<p style="margin: 0cm 0cm 0.0001pt; text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;"> </p>
<p style="margin: 0cm 0cm 0.0001pt; text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;">По своему составу и разновидности производства линолеум может быт гомогенным и гетерогенным. Рассмотрим отличия гомогенного и гетерогенного покрытия.</p>
<p style="margin: 0cm 0cm 0.0001pt; text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;"> </p>
<p style="margin: 0cm 0cm 0.0001pt; text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;"><strong>Гомогенный линолеум</strong> представляет собой однородное полотно, рисунок на котором расположен по всей толщине материала. Толщина такого покрытия может изменяться от 1,5 мм до 3 мм. Рекомендуется использовать это покрытие в помещениях с высокими показателями проходимости. Внешний вид гомогенного покрытия легко восстанавливается с помощью специальных видов мастики,которые способны придать ему лоск и ухоженный внешний вид.</p>
<p style="margin: 0cm 0cm 0.0001pt; text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;"> </p>
<p style="margin: 0cm 0cm 0.0001pt; text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;">Главное отличие такого покрытия от гетерогенного  - в составе гомогенного линолеума преобладает известь. В процессе производства ее добавляется довольно много. При эксплуатации она может проявляться в качестве белесых дорожек. Это может придать линолеуму некачественный и неухоженный вид.</p>
<p style="text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;">Данную смесь тяжело удалить даже при помощь специальных машин.Известь способна впитывать всю грязь с поверхности. Для избежания таких проблем на производстве наносят специальную защитнуюсмесь (полиуретановую). Для того, чтобы проверить и определить количество извести, необходимо просто согнуть линолеум.Известь в избытке, если проявляются белые полосы. Это свидетельствует о сниженном качестве продукции.</p>
<p style="margin: 0cm 0cm 0.0001pt; text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;">Стандартная ширина рулонов гомогенного линолеума - 2 метра.Реже можно встретить 3 и 4 метра. Такое покрытие можно приобретать в виде плиток.</p>
<p style="margin: 0cm 0cm 0.0001pt; text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;"> </p>
<p style="margin: 0cm 0cm 0.0001pt; text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;"><strong>Теперь рассмотрим отличия гетерогенного линолеума от гомогенного.</strong></p>
<p style="margin: 0cm 0cm 0.0001pt; text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;"> </p>
<p style="margin: 0cm 0cm 0.0001pt; text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;"><strong>Гетерогенный линолеум</strong> состоит из нескольких слоев. Толщина такого покрытия может достигать 6-7мм. При производстве применяется армирующий холст из стекловолокна. Рисунок на данном виде линолеума покрыт специальным слоем защитной пленки, что способствует предотвращению быстрого изнашивания покрытия. В добавках состава есть пластиковые гранулы и каменная крошка, иногда металлическая пыль. Благодаря этим добавкам покрытие становится более рельефным. Толщина обеспечивает износостойкость и прочность. Стоит учитывать это при выборе покрытия.</p>
<p style="margin: 0cm 0cm 0.0001pt; text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;"> </p>
<p style="margin: 0cm 0cm 0.0001pt; text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;">При выборе линолеума, первое, на что стоит обращать внимание – это его состав, назначение и технические характеристики. Для дачи подойдет морозостойкий линолеум, антистатический – для помещений с компьютерной техникой и т.д.</p>
<p style="text-align: justify; vertical-align: baseline; background-image: initial; background-attachment: initial; background-size: initial; background-origin: initial; background-clip: initial; background-position: initial; background-repeat: initial;">Также на гетерогенном покрытии присутствует подложка джута или войлока. Такое покрытие обладает высокими показателями теплоизоляции, амортизации и звукоизоляции. Такое покрытие имеет низкую стоимость по сравнению с гомогенным линолеумом.</p>


