<p class="MsoNormal" style="text-align: center;" align="center"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: black;">Линолеум классифицируют</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';"> по многим параметрам, включая разделение по виду основания: войлок и ПВХ.</span></p>
