<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 13.5pt;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">- Напольные пороги – применяются для крепления по местам стыков одинаковых или различных покрытий к полу. </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 13.5pt;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">- Т-образные пороги находят применение в креплении покрытий разных типов, а также способствуют лучшей стыковке покрытий. Модели порогов бывают как прямые, так и с закруглением.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 13.5pt;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Угловые накладки инсталлируются на край ступени. Они способствуют защите, как ступеней, так и покрытия от механического износа материала поверхности.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 13.5pt;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Разноуровневые пороги служат для крепления неодинакового по высоте покрытия, а также для хорошего прижатия материала покрытия к полу. Пороги, устанавливаемые снаружи помещения, могут быть оборудованы резиновыми вставками для уменьшения скользкости. Для легкости подбора пороги выпускаются различных размеров, цветов, изготавливаются из различных материалов для наилучшего сочетания с дизайном Вашего интерьера.</span></p>


