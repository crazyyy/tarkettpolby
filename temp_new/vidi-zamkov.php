 <h1>Виды замков</h1>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">С</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">уществует  много видов замков для сборки ламината. Большинство из производителей используют свои собственно разработанные виды замков, а остальные предпочитают пользоваться уже разработанными замковыми системами. Таких системы существует немало. Вот самые основные из них.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Замок SmartLock.</span></strong></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Имеет соединительные стыки с особой влагозащитной пропиткой. Благодаря особой комбинации соединительных стыков и влагозащитного слоя SmartLock наделен дополнительным уровнем защиты полов, иными словами он может выдерживать сильнейшие нагрузки любой тяжести. Плюс ко всему этому SmartLock не требует особых усилий при сборке.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Замок ProLoc</span></strong></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Этот вид замка применяется чаще всего в жилищных условиях, так как удовлетворяет высоким требованиям этого сектора. ProLoc обладает высшими техническими характеристиками, что позволяет ему быть самым надежным среди других видов замков. Имея превосходные характеристики,  ProLoc  очень адоптирован к  высоким нагрузкам, по этому его чаще используют в местах, где по напольным покрытиям ходят много людей. Также как и в </span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: EN-GB;" lang="EN-GB">SmartLock</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">,  ProLoc обладает особым влагоустойчивым слоем, не требует особых умений при сборке и монтаже, а отличительной особенностью является использование революционных тройной системы крепления, что в комбинации с влагоустойчивым слоем дает великолепную защиту от влаги и надёжность.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Замок PROClic</span></strong></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Это новая система замковых систем, разработанная компанией Egger (Эггер). Обладает особой геометрией профиля, которая позволяет легко и быстро укладывать ламинат, PROClic располагает таким качеством, как термоустойчивостью, устойчивость к влаги и нагрузкам, прочностью. Это замковая система воплощает в себе идею поц-гребня и клеевую технологию,  вдобавок он имеет комбинацию двух видов технологии соединений </span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: EN-GB;" lang="EN-GB">ProClickSistem</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Замок </span></strong><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: EN-GB;" lang="EN-GB">ClickXpress</span></strong></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Этот вид замков разработан компанией Balterio, имеет вполне изящный и эстетический вид к тому же  он легко укладывается, что  и радует профессионал и начинающих в этом деле. В этой системе замковых соединений используют уникальный метод уникальный метод ClickXpress , который обеспечивает прочное бесшовное соединение, иногда даже места стыков трудно разглядеть вблизи. Кроме того, благодаря такому виду замка, ничто не повредит ламинату при его разборке, будь-то ремонт или переезд, внешний вид при этом не изменится. Такой способ надежен, эффективен, и значительно сократит ваше время на укладку ламината.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Замок Uniclic</span></strong></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Uniclic- это метод, который отличается оригинальностью конструкции шип-паза, то есть соединение происходит лишь с помощью щелчка без использования клея. За счет этого сборка ламината сокращается на 50%. При работе с таким видом замка остается мало загрязнений помещения, и собирать его стало еще легче и приятней. Ну что сказать -  производитель придумал весьма оригинальный метод соединения, он сделал все что могло облегчить труд покупателя, при такой обновке время на укладку ламината значительно сокращается. В этой системе замков важно учитыватьсведения и обязательно использовать специальную накладку</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: EN-GB;" lang="EN-GB">UNCLIK</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">, иначе есть риск повреждения напольного покрытия.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Замок Megaloc</span></strong></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Один из самых лучших производителей напольных покрытий немецкая фирма Classen, выставила на продажу свою новинку  Megaloc. Это универсальная система замковых соединение, которая использует новаторские идеи инженеров </span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: EN-GB;" lang="EN-GB">Classen</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">, а именно соединения с помощью шпунтов и возможность производить сборку и монтаж в одиночку. Более того Megaloc содержит, превосходящею всех, влагостойкую защиту </span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: EN-GB;" lang="EN-GB">ISOWAX</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">, которая состоит из особых композиций смесей воска и дает абсолютную защиту от влажности.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Замок </span></strong><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: EN-GB;" lang="EN-GB">Loc</span></strong><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: EN-GB;" lang="EN-GB">Tec</span></strong></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Ламинат компании </span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: EN-GB;" lang="EN-GB">Wit</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">е</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: EN-GB;" lang="EN-GB">x</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">, используют запатентованный замок  Loc Tec, системы </span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: EN-GB;" lang="EN-GB">Unilin</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">, обладающая высокой прочностью, как и все виды замков, которые не нуждаются в клее. Ему присуща легкая сборка, не требующая особой профессиональной подготовки. Места стычки надежно защищены от влаги и обработаны специальной парафиновой мастикой. Особенностью Loc Tec является эксплуатация сразу после сборки.</span></p>
