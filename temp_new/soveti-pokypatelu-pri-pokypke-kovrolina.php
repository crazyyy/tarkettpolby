<h1>Советы покупателю при покупке Ковролина.</h1>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Во время ремонтного бума <strong>ковролин</strong> стал очень популярным. Ведь это то самое покрытие, которое поможет привнести в Ваш дом уют и комфорт, а также тепло и спокойствие. Почти все <strong>виды ковролина</strong> в настоящее время одинаковы по своему строению. Для того чтобы увидеть составляющие, достаточно взглянуть на ковролин в разрезе.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Итак, что мы видим в первую очередь на первичной основе - ворс. Во-вторых - прокладка и закрепляющего слоя и латекса. За качество ковролина отвечает материал, который входил в состав пряжи. Производство ковролина происходит из синтетических и натуральных волокон. Есть также и комбинированные варианты.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Наибольшей популярностью</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;"> пользуется натуральный шерстяной ковролин. Отличительным свойством такого покрытия является упругость, пожаробезопасность, слабая водопроницаемость, простая уборка. К недостаткам  относятся, безусловно, цена и отсутствие износоустойчивости.  Средняя цена за квадратный метр  - от 20 у.е.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Наилучшими из волокон для изготовления синтетического ковролина являются <strong>нейлоновые покрытия</strong>. Они качественно держат ворс, не оставляют на себе следы от мебели, легко чистятся. Нечувствительны к прямому свету. Срок службы, при правильной эксплуатации -10-15 лет.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Полиэстровые и акриловые  покрытия</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;"> уже значительно ниже по качеству, они менее долговечны (до восьми лет). Существует мнение, что даже несмотря на относительную дешевизну, это покрытие уже не отвечает современным требованиям. Дороже, но лучше.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Ковролиновое покрытие</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;"> можно разделить на <strong>три вида</strong>:</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">тканый</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;"> (самый качественный и стойкий)</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">тафтинговый</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;"> (разнообразит виды закрепления нитки, соответственно, отличается скорее эстетически, хоть и обладает высокой износостойкостью) <strong>иглопробиной</strong> высокой проходимости на резиновой основе область применения офисы, бары, рестораны.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; line-height: 12.75pt;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">петлевые</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;"> покрытия разделяются на одно- и многоуровневые. В многоуровневых вариантах у петель разная высота, что позволяет создать объемный рисунок, но они более сложны в очистке.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Ворсовый ковролин классифицируется</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;"> в зависимости от того, какова длина ворса. Покрытие с высоким ворсом сделает спальню уютней и теплей, гладкошерстное будет хорошо для кабинета или гостиной.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Сфера применения иглопробивного ковролина</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;"> - помещения с высокой проходимостью. Внешне безворсовый ковролин похож  на фетр или войлок. Обладает высокой устойчивостью к износу, Низкая цена и высокая шумоизоляция позволяют использовать его в  кинотеатрах и концертных залах.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Устройство ковролина.</span></strong></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Петлевой одноуровневый ковролин – один из самых простых  из всех существующих. Не стоит затягивать с очисткой ковролина, чтобы не допустить опускания грязи вглубь. Покрытие с коротким ворсов – велюр. Если ворс подрезать, он становится пушистым. Поверхность становится гладкой, схожей с бархатом. Можно выделить два вида велюра. Для дома хорош будет мягкий и шелковистый, а для офиса лучше упругий. Саксони  имеет зернистую поверхность.  Этот вид покрытия популярен из-за комфортабельности и элегантного вида. Но самым дорогим  является евросаксони с очень тонким и закрученным ворсом.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">А шег – покрытие с очень грубым ворсом. Называется фризе благодаря сильно закрученной нити. </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-ansi-language: FR; mso-fareast-language: RU;" lang="FR">C</span><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">ut-loop  - Ковровое покрытие, высокие петли которого разрезаны, а низкие ставлены без изменнений</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">О расцветке</span></strong></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Есть несколько способов окраски ковролина. Наиболее качественный – когда нить делается из однородной, уже окрашенной, синтетической массы. Второй, менее дорого вариант, когда окрашивается сама нить.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: 12.75pt;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Но способ, который позволяет краске стираться максимально быстро – нанесение краски на готовое покрытие с помощью трафарета.</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: 12.75pt;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Помните, что если ворс плотный, то его сложнее будет принять, он хуже протирается. Но и стоимость его несколько выше.</span></p>

