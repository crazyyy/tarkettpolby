 <h1>Укладка Гомогенного Гетерогенного Линолеума</h1>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Производство линолеума уже давно не стоит на месте, улучшаются его характеристики и дизайн. Один из вариантов современного напольного ПВХ покрытия – это гомогенный линолеум. </span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Поливинилхлоридявляется основой гомогенного линолеума. При производстве к нему добавляются пластификаторы и наполнители (песок, известь, доломит). Гетерогенный линолеум состоит из 4 слоев, а гомогенный имеет однородную структуру. </span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">После нескольких лет эксплуатации гомогенный линолеум не теряет своих первоначальных характеристик, расцветка остается неизменной. Гомогенные покрытия имеют очень большую область применения. </span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Процесс укладки состоит из таких этапов, как:</span></strong></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">- Расчет необходимого количества линолеума</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">- Выравнивание основания</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">- Монтаж гомогенного линолеума</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">- Обработка швов</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Допускается стелить линолеум одним полотном в помещениях площадью менее 20 м<sup>2.</sup>На больших площадях следует укладывать несколько полотен. При расчете количества обязательно учитывайте все особенности помещения, места под плинтусом и выступы.Стоит проследить, чтобы материал не отличался по цвету и не имел дефектов.</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Основание для укладки гомогенного линолеума, как и любого другого, должно быть хорошо подготовлено и выровнено при помощи шлифовальной машины или специальных смесей.Затем основание следует очистить от пыли и прогрунтовать. Укладка гомогенного линолеума осуществляется только на сухую поверхность.</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Перед монтажом гомогенный линолеум должен побыть в помещении не менее суток при комнатной температуре.После подгонки покрытия под размеры комнаты в местах стыковделаются напуски 2 см, около стен - 3 см. Излишки обрезаются (около стен и в местах стыков). Проклейка линолеума применяется в тех случаях, когда покрытие укладывается из нескольких частей.При проклейке обратная сторона покрытия должна быть прогрунтована.</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Гетерогенные покрытия неоднородны, состоят их нескольких слоев, каждый из которых выполняет определенную функцию. Верхний слой, благодаря полиуретановому напылению, обладает повышенной износостойкостью. Нижний слой из вспененного ПВХ обеспечивает покрытиюотличные звукоизолирующие свойства и комфортность. Для повышения прочности в может быть уложен специальный слой армирующей сети.</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Такой вид покрытий отлично подходит для лечебных учреждений, школ, ВУЗов,  торговых залов, развлекательных центров и административных помещений.</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Огромный ассортимент интересных и ярких расцветок, высокие показатели комфортности  и безопасности, отличные звукоизоляционные свойства делают гетерогенные ПВХ-покрытия идеально подходящими для детских учреждений.</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Для подготовки поверхности рекомендуется использовать в качестве стяжки специальную массу, которая самостоятельно выравнивается. Гетерогенное покрытие можно эксплуатировать уже через сутки, при условии, что оно было уложено правильно. </span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Сам процесс укладки включает такие процессы укладки, как:</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">- подготовка поверхности и демонтаж устаревшего покрытия </span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">- достижение гладкой и ровной поверхности</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">- укладка (проклейка гетерогенного линолеума с поверхностью)</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">- обработка стыков, их герметизация (сварка);</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">- монтаж плинтусов, очистка поверхности.</span></p>
<p class="MsoNormal" style="margin-bottom: 13.5pt; text-align: justify; line-height: normal; background: #FBFBFB; vertical-align: baseline;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Во время уборки все химические составы следует разводить в воде и наносить на линолеумследуя рекомендациям. Ножки столов лучше всего оборудовать специальными накладками, для долгой службы линолеума. </span></p>
