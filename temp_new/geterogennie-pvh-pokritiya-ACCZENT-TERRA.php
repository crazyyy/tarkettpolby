<p><font size="3" face="Times New Roman" color="black">&nbsp; &nbsp; &nbsp;Базовым структурным слоем гетерогенного покрытия, как правило, является стекловолоконный холст. Именно он в большей степени обеспечивает материал такими незаменимыми свойствами, как прочность и сопротивление деформациям и разрыву. Стеклохолст пропитывают пастой из поливинилхлорида для лучшего сцепления с другими слоями покрытия. </font></p>

