 <h1>Ламинат - достоинство и недостатки</h1>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Ламинат – покрытие для пола</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">, заменяющее поркетное покрытие. Пол из ламината имеет свои приимущества и недостатки. Отличие ламината от паркетных покрытий в том, что изготавливается не из природного материала как дерева,  а из плиты </span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';" lang="EN-US">HDF</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">пресуемой </span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: SAH-RU;" lang="SAH-RU">под давлением и высокой температурой, поверх которой наносят декоративный слой на основе полимеров и защищенный специальной пленкой. Внешние качества ламината отличить от покрытий из других материалов, например от паркетного покрытия, будет сложно. На сегоднящий день производство ламината очень развито, его внешнее покрытие отображает не только все сорта древесины, но и много других матариалов таких как гранит, мрамор, а также множество изображений и узоров.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Структура ламината</span></strong></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Стабилизирующий слой стабилизирует геометрические параметры всей плиты.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Основной слой – плита </span></strong><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';" lang="EN-US">HDF</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;"> (состоит из древесно – волокнистой плиты повышеной плотности). Производитель в основном использует плиты </span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';" lang="EN-US">HDF</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;"> повышенной влагостойкостью для избежания набухания в местак соединения при попадинии в них влаги.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Картон – крафт-бумага</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;"> пропитываемая смолой (на его поверхность накладывается пластик с изображением.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Декоративный слой</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;"> – обработанная особым способом бумага и отображающая множество видов натуральной древесины или других изображений.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Внешний слой</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;"> – защитный слой декоротивной бумаги, изготовленый из меламиновых смол, предавающий устойчивость к воздействию длительных нагрузок а также других видов износа ламината.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Преимущество ламината</span></strong></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Ламинатное покрытие устойчиво к давлению и нагрузкам на него. Не оставляет на себе вмятины а также другие воздействия от тяжелых предметов. Не беспокоясь можно передвигать мебель на колесиках и при этом не останется следов потертости, царапин. </span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Покрытие хорошо выдерживает температурные воздействия. Не требуется проводить полировки, чего нельзя сказать о паркетном покрытии. Достаточным уходом за ним будет протирание влажным материалом для мытья полов.</span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Также ламинатное покрытие считается экологическим  продуктом. Он не содержит химических средств которые могут повлиять на здоровье человека, а также не скапливает на себе пыль и бактерии и с увероностью может использовать во всем доме и офисных помещениях.</span><1/p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Также укладка панелей</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;"> <strong>ламината</strong> не составляет большого труда. Их можно укладывать на такие поверхности полов как:</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: SAH-RU;" lang="SAH-RU"> линолеум, деревяные и множество других включая в этот список и полы с подогревом.</span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: SAH-RU;" lang="SAH-RU">Про долгостойкость можно сказать, что при правильном мантаже ламината, он может прослужить более </span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">10 лет. </span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Также большой плюс ламинатное покрытие получает, за счет своей оптимальной цены, которая на порядок ниже, чем цена паркетного покрытия.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Недостатки ламината</span></strong></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">При нагрузки на ламинатное покрытие надо учитывать следущее. Покрытие разделается на несколько класов допускаемой ногрузки. </span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Классы покрытия</span></strong></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">21, 22, 23 подойдут для укладки в домах и квартирах. </span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">21 – имеет самый низкий класс нагрузки, подойдет для кладовой комнаты, спальной или в кабинете. </span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">22 - средний класс нагрузки, для укладки в гостиной или детской комнате. </span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">23 – сасмый высокий клас, может использоваьбся в каридоре, кухни или столовой комнате. </span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Цена на ламинат</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;"> зависит от его класса, естественно чем выше класс тем дороже его стоимость. Чтобы увеличеть срок службы ламината, надо покупать на класс выше чем тот которым вы можете обойтись.</span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Поверхность пола на которую будет укладываться ламенат должна быть прочной и сухой и правельна подготовлена, а также не допускаються перепады на поверхности пола больше 3мм на 1 м2. Укладка ламената на полы из дерева и линолиума сложная.</span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">При паподании большого количества влаги, покрытие может потерять свои качества и в этих местах начинать трескать, расыпаца и набухать. Важно при монтоже обработать соединение водо защитным средством.</span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">Также может появить скрип покрытия, пузырение и вздутие. Эти проблемы могут возникнуть при неправельном монтоже. Поэтому лутше воспользоваться помощью специалиста.</span></p>
<p class="MsoNormal" style="text-align: center;" align="center"> </p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">При покупки дешевого ламината </span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-ansi-language: RU;">можно сэкономить, но и качества покрытия будет очень низкое. Если же преобретать покрытие очень высокого качества то цена может совподать с ценой паркетного покрытия.</span></p>


