<CENTER><h1>Разновидность Линолеума в <p><br>зависимости от области применения<CENTER><h1>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;">Весь используемый линолеум по степени его использования может подразделяться на три категории – бытовой, полукоммерческий линолеум и коммерческий. Рассмотрим каждый вид по отдельности.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;"> </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;">Линолеум бытовой.</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;"> Он используется в жилых помещениях и имеет достаточно разнообразную палитру цветов. Как правило, этот вид линолеума состоит из трех слоев – подложка, основной слой, выполненный в определенном цвете и защитный слой. Такой линолеум обычно продается в рулонах с различной шириной – от 1,5 до 4 метров. </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;">Основной слой состоит из армирующего стекловолокна, что обуславливает его прочность. На основной слой с обеих сторон наносится специальный слой ПВХ, и уже сверху на него наносится определенный рисунок, покрытый защитным слоем. </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;">Обычно производители производят дополнительную обработку линолеума составом, повышающим срок службы поверхности и делающим процесс его мытья более легким. </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;">Немаловажную роль для срока службы имеет толщина защитного покрытия линолеума. Но не стоит забывать и о том, что чем толще этот слой, тем больше будет и цена на линолеум. Обычно для спальни или гостиной достаточно линолеума с защитным слоем толщиной 0,15 мм, для детской комнаты или столовой – слой 0,2 мм, для кухни или коридора подойдет линолеум с защитным слоем в 0,25 мм, ну а для подсобных помещений лучше всего использовать линолеум с защитным слоем около 0,5 мм. При покупке эту информацию можно всегда уточнить у продавца – консультанта. </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;"> </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;">При покупке линолеума также стоит обращать внимание на фирму – изготовителя, поскольку крупные компании производят свою продукцию без использования вредных для здоровья компонентов. Если же фирма не достаточно известна и ее продукция в среднем ниже в ценовом плане, то такой линолеум будет менее долговечным и может быстро потрескаться из-за наличия в его составе большого количества различных дешевых пластификаторов. При приобретении линолеума известного производителя вы также получите гарантию на определенное время. <strong>Наша компания советует купить линолеум таких известных и зарекомендовавших себя компаний как Таркетт, Ютекс, Синтерос, Комитек Лин, </strong></span><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-ansi-language: EN-US; mso-fareast-language: RU;" lang="EN-US">IVC</span></strong><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;"> (бельгия).</span></strong></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;"> </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;">Линолеум полукоммерческий.</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;"> Этот вид линолеума уместно использовать для напольных покрытий с помещениях со средней нагрузкой – маленькие кафе, номера гостиниц и т.д. Он намного прочнее обычного бытового линолеума и его гарантийный срок службы составляет в среднем от 7 до 20 лет. Защитное покрытие такого линолеума имеет повышенные износостойкие показатели. Продается он обычно в рулонах с шириной от 1,5 до 5 метров и толщиной до 3,4 мм. Основа может быть различной и влияет на его цену – ПВХ, текстиль. Такой линолеум быстро восстанавливает свою первоначальную форму при нагрузках и имеет достаточно разнообразную цветовую гамму. Кроме того этот вид линолеума можно приобрести с различной структурой – под паркет, под дерево, гладкий. Именно этот вид линолеума достаточно востребованный на рынке. </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;"> </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;">Линолеум коммерческий.</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;"> Данный тип линолеума используется в помещениях с большой проходимостью – магазинах, офисах, производственных помещениях. То самый дорогой вид линолеума, поскольку его защитный слой составляет от 0.5 до 0,8 мм и в его составе имеются различные добавки – изоляторы, антистатики и т.д. Окрашивание этого типа линолеума производят не только в одном слое, а по все    толщине, благодаря чему яркость цвета сохраняется долгие годы. Гарантийный срок службы этого линолеума составляет от 10 до 25 лет.  </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;">Коммерческий линолеум подразделяется на две категории – однослойный и многослойный. </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;"> </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;">Однослойный линолеум обычно составляет 3 мм в толщину и имеет однотонное цветовое покрытие. В более дорогом варианте в составе также имеется поливинилхлорид, в более дешевом варианте он заменен тальком или известняком. Такое покрытие можно увидеть в аэропортах, торговых центрах и т.д.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;"> </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;">Многослойный линолеум</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;"> обычно имеет толщину до 6 мм. Каркас этого линолеума выполнен из стеклохолста, а защитная пленка состоит из полиуретана. Для дизайна используются металлическая пыль, каменная крошка и другие подобные материалы. Толщина защитного соя составляет до 0,8 мм, по обуславливает его износостойкость. Многослойный линолеум имеет достаточно разнообразные рисунки и цвета. </span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; color: #222222; mso-fareast-language: RU;">Также коммерческий линолеум может быть специализированным и обладать специальными свойствами – звукоизоляцией, электроизоляцией, и т.д.</span></p>




