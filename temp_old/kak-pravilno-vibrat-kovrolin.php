<h1>Правильный выбор коврового покрытия</h1>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal; mso-layout-grid-align: none; text-autospace: none;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Ковролин</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';"> считаются одним из более известных материалов для пола, употребляемые и в административных помещениях и в квартирном дизайне. Знаменитыми они стали не так давно, однако украшения пола с помощью такого покрытия не считается современным открытием. Еще древние египтяне,  раскладывали на камнях маты, которые ткали из соломы. Ковровые покрытия пришли в моду  из США, где основная  часть пола изготавливается только с помощью укладывания коврового покрытия.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal; mso-layout-grid-align: none; text-autospace: none;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Для коридора</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';"> лучше всего подбирать ковровое покрытие с густым и низким ворсом. Оно должно обладать высокими грязеотталкивающими свойствами.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal; mso-layout-grid-align: none; text-autospace: none;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Кухня</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';"> - место для трапезы и приготовления пищи. На кухне не избежать появления пятен. Материал покрытия для таких помещений должен быть водонепроницаемым и грязеустойчивым.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal; mso-layout-grid-align: none; text-autospace: none;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Гостиная</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';"> - покрытие для этой данной комнаты в квартире должно обладать устойчивостью к загрязнению и сминаемости.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal; mso-layout-grid-align: none; text-autospace: none;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Спальная</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';"> - место для отдыха семьи. Покрытие для спальной комнаты должно обладать, в первую очередь, антистатическими свойствами. Хорошо подойдет покрытие с высоким и не очень густым ворсом.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal; mso-layout-grid-align: none; text-autospace: none;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Детская комната</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">. Чтобы ковровое покрытие в детской прослужило долго, онодолжно быть немарким и легко чистящимся.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal; mso-layout-grid-align: none; text-autospace: none;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Преимущества ковровых покрытий:</span></strong></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal; mso-layout-grid-align: none; text-autospace: none;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">1. Удобство во время  ходьбы - ковровое покрытие сделано из мягкого  материала, сгибаемого под ногами и приятно воздействующего  в момент ходьбы на тело. Раньше мягкий пол делали при помощи шкур, сплетенных вручную матов.Нагрузка на  опорно-двигательный аппарат уменьшается, когда человек ходит по мягкому полу. Ковровые покрытия - это не только нежные  ощущения, однако  и удобство в момент ходьбы, и полезность для самочувствия.</span></p>
<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal; mso-layout-grid-align: none; text-autospace: none;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">2. Шумопоглощение и звукоизоляция. Ковровое покрытие значительно уменьшает проникновение звуков, так и внутри (шаги), как снаружи. Степень звукопоглощения достигает 26 - 35%. Данный признак зависит от использованного материала и основы.</span></p>
<p class="MsoNormal" style="text-align: justify;"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Ковровое покрытие может быть:</span></strong></p>
<p class="MsoNormal" style="text-align: justify;"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">- <strong>Тканым.</strong> Они дорогостоящие, но самые прочные. Основа выглядит как сеточка. Рекомендуется использовать тканые покрытия в детской комнате.</span></p>
<p class="MsoNormal" style="text-align: justify;"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">- Тафтинговым</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">. Верх такого покрытия – это ворс либо петельки. С изнанки покрыт латексом. Именно такой ковролин подойдет для гостиной.</span></p>
<p class="MsoNormal" style="text-align: justify;"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">- <strong>Иглопробивным</strong>. Такое покрытие имеют прочную волокнистую основу, она прокалывается специальными иглами. Иглопробивной ковролин износостойки и не очень дорогой. Такой ковролин очень хорошо подойдет для кухни или прихожей, а также для офиса, бара ресторана, бильярда, кинотеатра.</span></p>
<p class="MsoNormal" style="text-align: justify;"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">- <strong>Флокированным</strong>. Это покрытия с полихлорвиниловой основой, на которой закреплены трехмиллиметровые ворсинки. Хорошо подойдет для ванной комнаты.</span></p>


