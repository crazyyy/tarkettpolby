<li style="margin:10px 0;"> <strong><span style="font-size: 13pt; line-height: 115%; font-family: 'Times New Roman','serif';">Сумахи </span></strong>- на них узор создается дополнительной третьей нитью, прокладывающейся между основой и утком косичкой</li>
<p><br>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Вторая большая группа – ворсовые ковры, </span></strong>отличается более сложным исполнением. Для их создания используют три нити: основу, уток и нить, которая образует ковровый узел, завязывающийся по заданному рисунку.</p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Также ковры в Минске, цены </span></strong>на которые стартуют всего от 500 000 тысяч рублей, можно классифицировать по другим критериям – материалу изготовления, наличию или отсутствию рисунка и т.д.</p>
