<p><br>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';"></span></strong>К самым востребованным натуральным материалам, которые издавна использовались для ткачества, относятся:</p> 
<li style="margin:10px 0;"> <strong><span style="font-size: 13pt; line-height: 115%; font-family: 'Times New Roman','serif';">Шелк. </span></strong>При всей нежности и кажущейся хрупкости материал отличается исключительными эксплуатационными свойствами. Он очень устойчивый к усадке, истиранию, и сминаемости, не деформируется и не меняет форму даже после долгих лет активного использования. Долговечные, красивые и роскошные ковры из натурального шелка долгое время украшали только дворцы китайских императоров, и лишь через столетия добрались до Европы. </li>
<li style="margin:10px 0;"><strong><span style="font-size: 13pt; line-height: 115%; font-family: 'Times New Roman','serif';">Шерсть. </span></strong>Благодаря плотной текстуре великолепно сохраняет тепло, а значит, защищает полы и часто стены жилища от мороза и пронизывающего ветра. Кроме того, шерсть – отличный звукоизолятор, материал впитывает влагу из окружающей среды, оставаясь сухим на ощупь, и затем выделяет ее, регулируя микроклимат помещения. </li>
<li style="margin:10px 0;"><strong><span style="font-size: 13pt; line-height: 115%; font-family: 'Times New Roman','serif';">Хлопок. </span></strong>Хлопковые изделия отлично держат форму и не растягиваются. Чаще всего из материала изготавливают основу ковров и комбинируют его с другим натуральным или искусственным сырьем.</li>
