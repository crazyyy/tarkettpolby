<p><br>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';"></span></strong>Основной критерий разделения изделия – наличие или отсутствие ворса (ЦИНОВКИ). В свою очередь, <a href="/kovri_bezvorsoviye.php">безворсовые ковры </a>делятся на несколько групп:</p>
