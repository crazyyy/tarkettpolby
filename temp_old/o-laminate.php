<CENTER><h1>Все что нужно знать о Ламинате</h1></CENTER>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Сравнительно молодое современное напольное покрытие (производится с 1977) – ламинат очень быстро завоевал рынок строительных материалов. Высокие качественные показатели износостойкости и приемлемая цена вот основы успеха и востребованности ламинированных полов. </span></p>
<CENTER><h2>Структура ламината</h2></CENTER>
<p> 
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';"> Основой ламината, является высокопрочная волокнисто-древесная плита с замком, скрепляющим панели. Бумага с нанесенным рисунком, пропитанная меламиновой смолой – это декоративный слой. 
Защитный слой – это пленочное высокопрочное покрытие из специальных смол придает ламинированной плите большей жесткости, защищает ее от деформации и повышает ее износостойкости в процессе использования.

Меламин (вид феноловой смолы) при воздействии на нее очень высокой температуры и давления, благодаря своей структуре превращается в прозрачное и прочное, как стекло покрытие. Верхний слой ламинированной плиты меламиновой смолы спрессовывается со слоем крафт-бумаги и декоративной пленкой. Этот процесс производится давлением в сотни бар и при температуре больше 300 С. Таким образом достигается большая устойчивость к всевозможным механическим воздействиям и прочность этого строительного материала.
 </span></p>
<CENTER><h2>Достоинства ламината</h2></CENTER>
<p> 
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Ламинат выдерживает тяжелую мебель, непотушенный, случайно упавший окурок не оставляет никаких следов, которые остаются на других видах напольных покрытий.

Пролившийся лак, легко удаляется ацетоном. Карандашные рисунки, фломастером и мелками с любовью нарисованные детьми легко снимаются пи влажной уборке.

Домашние животные не оставляют царапин от когтей, а поездки детей на роликах или велосипеде ламинированному полу совершено не страшны.

Водоотталкивающий 10 мм слой выдерживает температурные колебания.

Уход за ламинатом прост – влажная уборка и пылесос легко справляются с ежедневным мусором. </span></p>    
 <p> </p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">При покупке ламината известных брендов необходимо выбрать материал со светостойкостью выше 6, тогда ламинированный пол не будет подвергаться выгоранию под воздействием солнечных лучей. Ламинированная конструкция на протяжении долгого периода совершенно не потеряет свой первозданный вид.

Качество несущей панели напрямую зависит прочность напольного покрытия. При изготовлении производители известных брендов используют высокой плотности ДВП и ДСП, произведенные из твердой древесины.

Панели нарезаются высокоточными инструментами, не допускающие на срезах ламината трещин, сколов и надломов, которые при сборке на стыках испортят внешний вид всей конструкции.

Ламинированные плиты на 90% состоят из дерева и для эксплуатации в помещениях с повышенной влажностью использовать нельзя. Нижний слой из-за постоянной влажности вздуется и легко деформируется.

Внешним видом современный ламинат похож на натуральный паркет. Ламинированное покрытие чаще всего монтируется с подогревом. </span></p>
 <p> </p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">При сравнении паркета и достоинства ламинированного пола бесспорны. Ламинат намного дешевле паркетной натуральной доски и легко собирается в конструкцию.

При производстве ламинированной плиты высокого качества используют крафт – бумагу, как дополнительный слой, между декоративной пленкой и основной плитой. Это позволяет улучшить несущей панели ее фиксацию и повышается шумовая изоляция и пожаростойкость всей напольной конструкции. 

Некоторые брендовые ламинированные плиты имеют до 4-х слоев крафт–бумаги, такая продукция строительного материала значительно выше по стоимости.</span></p>


