 <h1>Как ухаживать за ковролином?</h1>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;">Сегодня ковролин</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;"> известен всем. Благодаря ряду преимуществ он практически заменил привычные нам ковры.</span><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: #222222;"><br /> <span style="background: #FCFDFD;">Но если вы хотите, чтобы он прослужил вам не один десяток лет в отличном состоянии, за ним нужно постоянно ухаживать. </span></span></p>
<p class="MsoNormal" style="line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;">Ковролин изготавливают</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;"> из различных материалов (ненатуральных и искусственных), некоторые из ни более устойчивы, а некоторые менее устойчивы к деформациям. Поэтому для каждого типа коврового покрытия необходима своя чистка.</span><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222;"><br /> <!--[endif]--></span></p>
<p class="MsoNormal" style="line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;">Но для всех типов существуют общие требования по уходу:</span></strong><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222;"><br /> <!--[endif]--></span></strong></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;">1. Перед первой чисткой нужно внимательно изучить рекомендации производителя и всегда им следовать;</span></p>
<p class="MsoNormal" style="line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;">2. При попадании любого пятна на покрытие необходимо сразу же удалить его во избежание глубокого въедания в ворс;</span><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222;"><br /> <!--[endif]--></span></p>
<p class="MsoNormal" style="line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;">3. У входной двери желательно расстелить коврик, чтобы большая часть грязи и пыли оседала именно не нем;</span><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222;"><br /> <!--[endif]--></span></p>
<p class="MsoNormal" style="line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;">4. Пылесосить ковролин нужно 2-3 раза в неделю. Влажные уборки нужно проводить редко, влага негативно влияет внешний вид покрытия.</span><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222;"><br /> <!--[endif]--></span></p>
<p class="MsoNormal" style="line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;">Тщательного</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;"> и внимательного ухода требует <strong>ворсовый ковролин</strong>, поскольку в его ворсинках скапливается пыль, мусор. Чистить его нужно только мягкой щеткой, поскольку жесткая щетка может повредить структуру покрытия. Покрытие потеряет ухоженный и привлекательный вид, если регулярно его не чистить: ворсинки быстро сотрутся, потускнеют, склеятся между собой. Согласитесь, вид очень непрезентабельный. Стоит подчеркнуть, что ворсовое покрытие желательно чистить сухим способом, поэтому регулярно проводите сухую чистку пылесосом.</span><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222;"><br /> <!--[endif]--></span></p>
<p class="MsoNormal" style="line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;">Длинноворсовый ковер</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;"> долго сохнет, поэтому не стоит усердно, не жалея воды, чистить его. Существует возможность, что ворс приобретет крайне неприятный запах.</span><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222;"><br /> <span style="background: #FCFDFD;">Ковролину несколько раз в месяц необходима глубокая чистка. Для этого идеально подойдет специальный сухой порошок, микрогранулы которого имеют структуру губки. При распределении порошка по всему ковролинумикрогубки меняют свой цвет на темный. Это означает, что они впитали в себя всю грязь и пыль. Лишь только после потемнения можно собирать порошок пылесосом.</span><br /> <!--[endif]--></span></p>
<p class="MsoNormal" style="line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;">В случае,</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;"> если на ковровом покрытии появилось <strong>пятно</strong>, нужно как можно быстрее его удалить. В специализированных магазинах в продаже имеются специальные пятновыводители. Если ваше покрытие посажено на клей, чистку производите аккуратно, чтобы случайно не повредить вторичную основу. Запомните, что застарелые пятнка очень сложно удалить даже специальным средством, поэтому нужно своевременно удалять их.</span><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222;"><br /> <span style="background: #FCFDFD;">Раствор нашатырного спирта способен сказочно изменить внешний вид покрытия, он заиграет новыми красками. В литре воды разведите 3 столовые ложки нашатыря и щеткой наносите раствор на покрытие. Вы сразу увидите результат.</span><br /> <!--[endif]--></span></p>
<p class="MsoNormal" style="line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;">Для ковролина на резиновой</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;"> основе подойдет чистка пылесосом 3 раза в неделю, не реже; влажной уборке резиновое покрытие подлежит раз в месяц. Влажную уборку можно проводить обычной тряпкой, губкой, моющим пылесосом в сочетании с моющим средством. Так же, как и ворсовое покрытие, резиновое нужно тщательно просушить после влажной уборки, потому что оно так же долго сохнет.<span class="apple-converted-space"> </span></span><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222;"><br /> <!--[endif]--></span></p>
<p class="MsoNormal" style="line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222; background: #FCFDFD;">Альтернатива влажной уборке <strong>- чистка специальной пеной.</strong> Со временем пена набирает популярность среди покупателей, которые признают ее самым качественным способом уборки. Нужно распределить пену с помощью распылителя на покрытие, тщательно растереть его щеткой и дождаться, пока оно полностью высохнет. После полного высыхания нужно удалить пену пылесосом. Пена не сильно увлажняет покрытие, улучшает качество очистки.</span><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif'; color: #222222;"><br /> <!--[endif]--></span></p>
