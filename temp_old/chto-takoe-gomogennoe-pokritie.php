<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Толщина покрытия может варьироваться от 1,5 до 3 мм. В его состав входят антисептики. Такой линолеум обладает значительной упругостью, которая ощущается под ногами при ходьбе. Его рекомендуется использовать в помещениях с повышеннойпроходимость. Это могут быть офисы, школы, спортивные учреждения, развлекательные центры и т.д.</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Чем толще покрытие, тем больше нагрузок оно способно выдержать.Существует 2 вида такого покрытия: на основе вспененного ПВХ и без основы. На основе ПВХ линолеум несколько мягче, не такой же прочный.</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Основные преимущества гомогенного покрытия:</span></strong></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">- рисунок нанесен по всей толщине</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">- высокие показатели прочности и износостойкости</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">- высокие показатели упругости, эластичности и гибкости</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">- покрытие антистатично</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">- долговечность и низкая истираемость покрытия</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">- прост в уходе.</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Недостатками</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';"> можно назвать  небольшой ассортимент дизайна (крапинка, абстракции и однотонные варианты), высокую цену и строгие требования к основанию, на которое укладывается покрытие.</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Основание должно быть идеально ровным. Любые выступы и неровности не останутся незамеченными. Они могут стать причиной появления дефектов и наибольшего истирания. Стяжка поможет сделать основание таким, как необходимо для укладки гомогенного линолеума.</span></p>
