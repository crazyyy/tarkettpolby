<p class="MsoNormal" style="margin-bottom: .0001pt; text-align: justify; line-height: normal; mso-layout-grid-align: none; text-autospace: none;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Промышленные напольные системы ПВХ</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">. Такой пол складывается из плиток. Крепкий слой способен выдержать большие нагрузки, возникающие при движении погрузчиков. Используется такое покрытиедля производственных, складских помещений, офисов, автомастерских.</span></p>
