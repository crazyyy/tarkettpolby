<CENTER><h1>Классификация Линолеума</h1></CENTER>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">При выборе <a href="http://tarkettpol.by/linoleym.php">линолеума</a> </span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';"> не стоит спешить, необходимо грамотно подойти к выбору, ведь прогресс не стоит на месте и каждый раз появляются все больше и больше новинок с разными эксплуатационными параметрами, о которых нужно знать хоть какое-то представление. </span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">И так при выборе <strong>линолеума</strong> необходимо знать, что линолеум разделяют на группы: натуральные, поливинилхлоридные (ПВХ), алкидные, нитроцеллюлозные и резиновые. Каждая из этих групп имеет свои особенности и характеристики, и классифицируют их по материалу и области применения.</span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Линолеумное покрытие может быть на основе или без нее. </span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Линолеум без основы</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';"> считается однослойным тонким полотном и по всей ширине рисунком. Тонкий линолеум применяется как правило в помещениях с небольшой загрязненностью и повышенной влажностью. Так как линолеум слишком тонкий укладывать его необходимо на идеально ровное основание, а в некоторых случаях приклеивать по всей площади. Часто на такой линолеум наносят специальное покрытие для защиты и продавливания срока службы, а так же для облегчения уборки. Преимущество такого линолеума - низкая цена, недостатком является маленький срок службы (5-7 лет).</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';"> <a href="http://tarkettpol.by/linoleym-tarkett-Premium.php">Линолеум на вспененной</a> </span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';"> основе достаточно плотный и имеет толщину в пределах от 2,5 до 5 мм. Применяют его в местах с наибольшей загрязненностью, где требуется частая влажная уборка. Укладывается на любой поверхности, стыки полотна заваривают либо склеивают специальным жидким составам. Прослужит такой линолеум, при правильной эксплуатации до 10 лет и более.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';"> <a href="http://tarkettpol.by/linoleym-tarkett-Idillia.php">Линолеум на теплой основе</a> </span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';"> относится к отдельной группе. Такой вид линолеума состоит из полимерного слоя с однородным рисунком на всю площадь. Полимерный слой приклеивается к натуральным или синтетическим войлочную основу, и таким образом общая толщина составляет в приделах 3.4 мм. <strong>это такие коллекции как Идиллия, Мегаполис, Император, Комфорт.</strong> Такой линолеум довольно- таки мягкий, хорошо сохраняет тепло и звукоизоляцию. Укладывать такой вид линолеум рекомендуется одним полотном без стыков, если без стыков укладка невозможна, тогда стыки необходимо тщательно загерметизировать. В случае попадания влаги в стыки может привести к загниванию основы. Так же необходимо учесть при укладке данного вида линолеума о возможном его расширению. Укладывая такой вид линолеума под плинтус между стенкой и краем линолеума, необходимо оставить небольшой зазор, чтобы избежать волнообразования на линолеуме.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Основными показателями качества линолеума</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';"> считается его водостойкость, плотность, горючесть, износоустойчивость, а так же ряд других показателей. Многие производители различных видов линолеума выпускают свою продукцию с показательными пиктограммами, позволяющие определить особенности материала без дополнительный переводов.</span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Технологии не стоят на месте и в последнее время появился огромный выбор различного линолеума с наилучшими потребительским показателями. Разработанные они специально для особых эксплуатационных зон.</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">По классификации области применения линолеу</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">м так же разделяю на некоторые виды: линолеум общего назначения (повышенная износоустойчивость и разнообразие цветовой гаммы) токопроводящие и антистатические, спортивные (обеспечивают безопасность падения и хороший отток мяча) и медицинский (в составе такого линолеума входят вещества препятствующие появления грибков и бактерий)</span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Как правило</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">, в жилых помещениях используют различные <strong>типы и виды линолеума.</strong> Линолеум считается наиболее популярным напольным покрытием, объясняется это тем, что является наиболее прочным, эстетичным и не привередливым в уходе. </span></p>
<p> </p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif';">Правильно подобрав покрытие на пол - одно из главных правил длительного использования. Прочные свойства и отличные декоративные показатели дают возможность конкурировать с ковролином и паркетом. По сроку эксплуатации линолеум не уступает своим конкурентам, он легок в простоте укладки и дешевизне. </span></p>


