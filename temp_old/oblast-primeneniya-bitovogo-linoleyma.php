<CENTER><h1>Область применения бытового<p><br>линолеума<CENTER><h1>
<p class="MsoNormal" style="text-align: center;" align="center"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: black;">Область применения бытового линолеума «Бытовым» прийнято называть тот линолеум толщина защитного слоя котрого составляет от 0,15 мм до 0,35 мм. По износоустойчивости это 21 класс- для редко посещаемых помащений, на пример спалень, детские комнаты,  22 класс – для помещений с высокой проходимостью  на пример гостиные, и 23 класс – для самого активного посещения, на пример кухня, коридоры. Если бережно относится к бытовому линолеуму и беречь его от разрывов и прожогов во время эксплуатации, он достойно прослужит до 8-15 лет и практически не утратит свой внешний вид.</span></strong></p>
<p><br>
<p class="MsoNormal" style="text-align: center;" align="center"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: black;">В некоторых случаях бытовой линолеум подойдет и для общественных мест с умеренной посещаемостью, таких как учебные классы, кабинеты, кафетерии, небольшие торговые точки и т. д. главное чтобы в этом случае линолеум омел достаточно прочную основу, толщиной 2-3 мм.</span></p>
<CENTER><h2>Виды бытового линолеума и чем они отличаются?<CENTER><h2>
<p class="MsoNormal" style="text-align: center;" align="center"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: black;">Современный рынок бытовых линолеумов предлагает 2 вида: тонкий и толстый. Отличаются они друг от друга своей многослойной структурой, а так же физическими характеристиками.</span></p>
<p class="MsoNormal" style="text-align: center;" align="center"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: black;">Тонкий – состоит примерно из 3-4 слоев: защитный ПВХ слой с рисунком и холстяного-вспененного ПВХ, возможен вариант подложки. Толщина такого линолеума 1-1,5 мм.
<p class="MsoNormal" style="text-align: center;" align="center"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: black;">Толстый – состоитиз 5 слоев: защитного ПВХ  слоя с рисунком, холстяного, стеклохолста и подложки. Тут толщина, конечно же, больше 3,5-4 мм.  Благодаря наличию стеклохолста такой линолеум не растягивается и болеепрочен в эксплуатации. Как правило, в толстом линолеуме применяют вспененную подложку.</span></p>
<p><br>
<p class="MsoNormal" style="text-align: center;" align="center"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: black;">Виды подложек (основ) Сегодня используется 3 вида подложки: вспененнаяиз ПВХ, полиэстеровая (войлочная) и джутовая (тканевая).</span></p>
<p><br>
<p class="MsoNormal" style="text-align: center;" align="center"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: black;">Вспененная из ПВХ – на вид напоминающая губку, применяется для изготовления толстого линолеума, она придает прочность и долговечность покрытию. Кроме того, линолеум на вспененной подложке обладает хорошей тепло- и звукоизоляцией. А так же может скрыть небольшие неровности пола.</span></p>
<p><br>
<p class="MsoNormal" style="text-align: center;" align="center"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: black;">Полиэстеровая (войлочная) - так же применяется для толстого линолеума, как и вспененнаяиз ПВХ обладает хорошей тепло- и звукои золяцией. Пол становится мягким и теплым. Но линолеум на войлоке не подходит для помещений с повышенной влажностью или с вероятностью затопления (санузел, кухня и т. д.), так как поддействием влаги волок отсыреет и испортится.</span></p>
<p class="MsoNormal" style="text-align: center;" align="center"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: black;">Джутовая (тканевая) – на обратной стороне линолеума она выглядит как холщевая (тканевая) сетка. Чащевсего джут применяется для натуральних линолеумов, а тканевая сетка для синтетических, тонких линолеумов.</span></p>
<p class="MsoNormal" style="text-align: center;" align="center"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: black;">Есть мнение, что войлочная подложка теплее и лучше вспененной, это не обязательно. Все зависит от толщины подложки, ее плотности и качества. Разумнее сравнивать конкретные два продукта, чтобы решить, какой из отобранных образцов будет болем мягким, теплым и долговечным. К не достаткам натуральних подложек можно отнести их предрасположенность к усадке при длительном давлении или с течением времени, при этом искусственные волокна и пена не обладают таким свойством.</span></p>
<p class="MsoNormal" style="text-align: center;" align="center"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; color: black;">Современный рынок бытовых линолеумов позволяет выбрать именно тот вид, который подойдет для конкретного помещения с определенными требованиями по эксплуатации, и уложиться в запланированный бюджет.</span></p>

