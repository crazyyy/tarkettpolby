<p class="MsoNormal" style="text-align: justify; line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Мы осуществляем услуги по укладке разнообразных видов ковролина в любых помещениях и на любое основание.</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';"> В случае, когда основание не пригодно для монтажа (имеются неровности на длине 2м превышающие несколько миллиметров), наши специалисты выполнят ремонт основания (выравнивание неровностей, стяжка). Стоимость работ наших специалистов при укладке ковролина в жилых помещениях составит  2.5$  за  1м<sup>2</sup>. Результат работы будет радовать Вас долгие годы.</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Как правило, при осуществлении ремонта, полу отводится второстепенное значение, в отличие от стен и потолка. Но ведь именно пол принимает на себя самые большие нагрузки. Материалы для укладки должны использоваться хорошего качества, чтобы покрытие долго не истиралось и не теряло формы. Необходим грамотный расчет размера помещений для точного определения необходимого количества ковролина.Размеры желательно увеличить на 10-15 см, для избежания неприятных непредвиденных ситуаций.Покрытие будет служит долго, если все работы по укладке будут произведены качественно.  Выделяют такие основные способы укладки ковролина, как:</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">- укладка без фиксации – свободная укладка</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">- укладка на двусторонний скотч по все периметру помещения</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">- укладка с полной фиксацией к полу (прямая проклейка)</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">- двойная проклейка (если ковролин укладывается на подложку)</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">- стретчинг – без клея и подложки.</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Фиксация на двусторонний скотч.</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';"> Такой метод предполагает такую раскатку ковролина, чтобы края немного заходили на стены (5-10см). Далее от центра во все стороны ковролин распрямляется специальным валиков, а лишнее обрезается и закрепляется плинтусом.На проходах ковролин крепят металлическими порогами. У такого способа укладки много недостатков: возможно возникновение занятостей или пузырей, невозможность использования для очистки поющего пылесоса и т.д.</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Приклеивание.</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';"> При данном методе укладки могут возникнуть проблемы при желании замены ковролина – клей может плохо удаляться с поверхности пола. Такой способ отлично подходит для офисных помещений, где достаточно высокая проходимость.Основание, как и при любом другом способе монтажа, должно быть подготовлено. Неровности должны быть устранены. Если основание деревянное – оно должно быть сухим и чистым. Клей лучше наносить специальными зубчатыми шпателями для равномерного распределения по поверхности.</span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">При двойной проклейке сначала к полу приклеивается подложка (толщина до 10мм), а затем приклеивается сам ковролин. Однако, такой метод неприемлем, если в квартире установлены теплые полы.  </span></p>
<p class="MsoNormal" style="text-align: justify; line-height: normal;"><strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';">Стретчинг.</span></strong><span style="font-size: 12.0pt; font-family: 'Times New Roman','serif';"> При данном методе монтажа вдоль стен, в которую вбиты гвозди под углом 45 градусов. Ковролин закрепляется и натягивается при помощи специальныхинструментов. Такой метод предполагает обязательное наличие подложки и осуществляется исключительно специалистами. Период эксплуатации покрытия, которое уложено таким способом, значительно возрастает.</span></p>
