 <h1>Что такое Гетерогенное покрытие</h1>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Гетерогенное покрытие</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;"> (коммерческий линолеум) имеет довольно сложный состав. Видимый на всей поверхности рисунок защищен слоем прозрачного чистого ПВХ.           </span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">У гетерогенного покрытия <strong>толщина</strong> такого <strong>износостойкого равна примерно 0,7 мм</strong>. Этот слой несет на себе все нагрузки по истираемости покрытия. От его качества зависит срок эксплуатации линолеума. Кроме явного рисунка и чистого слоя ПВХ, в покрытие имеется армирующая сетка из стекловолокна. Благодаря такой сетке покрытие не следует раскатывать перед настилом что значительно упрощает и скоряет процесс укладки на крупных объектах.</span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Стыки варятся специальным шнуром, который можно подобрать под цвет модели. Как и из гомогенного, из гетерогенного покрытия могут быть вырезаны различные рисунки, например, логотип фирмы и т.д. Такой рисунок придаст полу яркость и индивидуальность! Срок службы - около 10-15 лет. </span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Толщина последнего слоя влияет на устойчивость покрытия к истиранию. У этого слоя маленькие поры и сначала уход и очистка не вызывают трудностей. Однако при повреждении верхнего слоя в поры начинает забиваться грязь, что немного усложняет процесс очистки. </span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Генеральная уборка</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;"> такого покрытия осуществляется с помощью специальных средств, следуя всем указанным инструкциям производителя: наносится  моющее средство, используются роторные машины, применяется белыйпад. Затем покрытие убирается и промывается, чтобы на нем не осталась щелочь.</span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">При уборке желательно применять нежесткие щетки, не использовать тяжелые роторные машины, из-за них покрытие может подвергнутся деформации от нагрузки. Следует аккуратно производить очистку, декоративный верхний слой гетерогенных покрытий достаточно тонкий, во время уборки можно его повредить, если не соблюдать все требования. </span></p>
<p class="MsoNormal"><strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Коммерческий гетерогенный линолеум</span></strong><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;"> обладает высокой прочностью, не подвержен сильному истиранию и действию разбавленных кислот и образованию пятен. Но для очистки противопоказано использование растворителей .  Не боится точечных нагрузок: острых женских каблуков и колес тележек. </span></p>
<p class="MsoNormal"><span style="font-size: 12.0pt; line-height: 115%; font-family: 'Times New Roman','serif'; mso-fareast-font-family: 'Times New Roman'; mso-fareast-language: RU;">Такой линолеум соответствует самым высоким требованиям пожарной безопасности: не поддерживает горение и не поджигается.</span></p>
